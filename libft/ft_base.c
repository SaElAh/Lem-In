#include "includes/libft.h"

int	ft_base(const char c)
{
	if (c == 'o')
		return (8);
	else if (c == 'u' || c == 'i' || c == 'd')
		return (10);
	else if (c == 'x' || c == 'X')
		return (16);
	else
		return (0);
}
